    public function getByUser(User $user)
    {
        $qb = $this->createQueryBuilder("c")
            ->where(':user MEMBER OF c.users')
            ->setParameters(new ArrayCollection([
                new Parameter('user', $user),
            ]));

        return $qb->getQuery()->getResult();
    }

